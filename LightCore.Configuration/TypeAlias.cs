namespace LightCore.Configuration
{
    ///<summary>
    /// Represents a type alias for registrations.
    ///</summary>
    public class TypeAlias
    {
        ///<summary>
        /// Gets or sets the type alias.
        ///</summary>
        public string Alias
        {
            get;
            set;
        }

        ///<summary>
        /// Gets or sets the type value for this alias.
        ///</summary>
        public string Type
        {
            get;
            set;
        }
    }
}